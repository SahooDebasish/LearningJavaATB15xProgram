package ex_07_Increment_Decrement_Op;

public class Lab_078_De_P2 {
    public static void main(String[] args) {
        int a = 10;
        int result = --a;
        System.out.println(result);
        System.out.println(a);
        // line -7 --> 9
        // line - 8 --> 9
        // line | a | expression
        // 7   | 9
        // 8   | 9
    }
}
