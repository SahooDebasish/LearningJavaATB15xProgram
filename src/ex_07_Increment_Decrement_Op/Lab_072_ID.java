package ex_07_Increment_Decrement_Op;

public class Lab_072_ID {
    public static void main(String[] args) {
        int a = 10;
        int result = ++a;
        System.out.println(result);

        // ERT ( Exp and Result Table)
        // LNo |  a | result
        // 5 | 10  | NA
        // 6 | 11  | 11
    }
}
